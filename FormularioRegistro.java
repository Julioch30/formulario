    private void registrarUsuario() {
        String nombre = txtNombre.getText();
        String apPaterno = txtApellido_Paterno.getText();
        String apMaterno = txtApellido_Materno.getText();
        String correo = txtCorreo.getText();
        String contrasena = txtContrasena.getText();

        if (nombre.isEmpty() || apPaterno.isEmpty() || apMaterno.isEmpty() || correo.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        boolean exito = ConexionBD.insertarUsuario(nombre, apPaterno, apMaterno, correo, contrasena);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente.");
            txtNombre.setText("");
            txtApellido_Paterno.setText("");
            txtApellido_Materno.setText("");
            txtCorreo.setText("");
            txtContrasena.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el usuario.");
        }
    }

